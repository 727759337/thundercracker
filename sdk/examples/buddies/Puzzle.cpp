Puzzle::Puzzle()
    : mBook(0)
    , mTitle(NULL)
    , mClue(NULL)
    , mCutsceneLineStart()
    , mCutsceneLineEnd()
    , mBuddies()
    , mNumCutsceneLineStart(0)
    , mNumCutsceneLineEnd(0)
    , mCutsceneEnvironemnt(0)
    , mNumShuffles(0)
    , mNumBuddies(0)
    , mPiecesStart()
    , mPiecesEnd()
{
    for (unsigned int i = 0; i < arraysize(mCutsceneLineStart); ++i)
    {
        mCutsceneLineStart[i].mSpeaker = 0;
        mCutsceneLineStart[i].mText = NULL;
    }
    
    for (unsigned int i = 0; i < arraysize(mCutsceneLineEnd); ++i)
    {
        mCutsceneLineEnd[i].mSpeaker = 0;
        mCutsceneLineEnd[i].mText = NULL;
    }
    
    for (unsigned int i = 0; i < arraysize(mBuddies); ++i)
    {
        mBuddies[i] = 0;
    }
}   

////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////

void Puzzle::Reset()
{
    mBook = 0;
    mTitle = NULL;
    
    for (unsigned int i = 0; i < arraysize(mCutsceneLineStart); ++i)
    {
        mCutsceneLineStart[i].mSpeaker = 0;
        mCutsceneLineStart[i].mText = NULL;
    }
    mNumCutsceneLineStart = 0;
    
    for (unsigned int i = 0; i < arraysize(mCutsceneLineEnd); ++i)
    {
        mCutsceneLineEnd[i].mSpeaker = 0;
        mCutsceneLineEnd[i].mText = NULL;
    }
    mNumCutsceneLineEnd = 0;
    
    mCutsceneEnvironemnt = 0;
    
    mClue = NULL;
    mNumShuffles = 0;
    
    for (unsigned int i = 0; i < arraysize(mBuddies); ++i)
    {
        mBuddies[i] = 0;
    }
    mNumBuddies = 0;
    
    for (unsigned int i = 0; i < kMaxBuddies; ++i)
    {
        for (unsigned int j = 0; j < NUM_SIDES; ++j)
        {
            mPiecesStart[i][j] = Piece();
            mPiecesEnd[i][j] = Piece();
        }
    }
}   

