namespace Buddies {

///////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////

class Piece
{
public:
    enum Attribute
    {
        ATTR_NONE = 0U,
        ATTR_FIXED,
        ATTR_HIDDEN,
        
        NUM_ATTRIBUTES
    };
    
    Piece();
    Piece(
        int buddy,
        int part,
        bool must_solve = false,
        Attribute attribute = ATTR_NONE);
    
    int GetBuddy() const;
    void SetBuddy(int buddy);
    
    int GetPart() const;
    void SetPart(int part);
    
    int GetRotation() const;
    void SetRotation(int rotation);
    
    bool GetMustSolve() const;
    void SetMustSolve(bool mustSolve);
    
    Attribute GetAttribute() const;
    void SetAttribute(Attribute attribute);
    
    bool Compare(const Piece &rhs) const;
    
private:
    unsigned int mBuddy  : 5;
    int mPart            : 3;
    int mRotation        : 3;
    bool mMustSolve      : 1;
    Attribute mAttribute : 3;
};

///////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////

}

